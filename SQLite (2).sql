-- Удаление существующих таблиц, чтобы избежать конфликтов
DROP TABLE IF EXISTS Клиенты;

-- 1. Создание таблицы Клиенты
CREATE TABLE Клиенты (
    ID_Клиента INTEGER PRIMARY KEY AUTOINCREMENT,
    ФИО_Клиента VARCHAR(100),
    Телефон_Клиента VARCHAR(15)
);

-- 2. Процедура вставки данных
INSERT INTO Клиенты (ФИО_Клиента, Телефон_Клиента)
VALUES ('Иван Иванов', '+79991234567');

-- Проверяем, что запись вставлена
SELECT * FROM Клиенты;

-- 3. Подсчёт количества записей с фильтром
-- Фильтруем по ФИО клиента (пример фильтра)
SELECT COUNT(*) AS Количество
FROM Клиенты
WHERE ФИО_Клиента = 'Иван Иванов';

-- 5. Заполнение таблицы случайными данными
-- Используем функцию RANDOM() для генерации случайных данных
WITH RECURSIVE random_data(n) AS (
    SELECT 1
    UNION ALL
    SELECT n + 1 FROM random_data WHERE n < 10 -- Укажите количество записей
)
INSERT INTO Клиенты (ФИО_Клиента, Телефон_Клиента)
SELECT 'Клиент_' || n, '+7999' || ABS(RANDOM() % 9000000 + 1000000)
FROM random_data;

-- Проверяем, что случайные данные вставлены
SELECT * FROM Клиенты;